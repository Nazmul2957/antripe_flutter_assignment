class Contact {
  final String id;
  final String name;
  final String phone;
  final String categoryId;
  final String avatarUrl;
  final String subtitle;
  final String status;

  Contact({
    required this.id,
    required this.name,
    required this.phone,
    required this.categoryId,
    required this.avatarUrl,
    required this.subtitle,
    required this.status,
  });
}